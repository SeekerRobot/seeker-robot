// Frame Registry
// ============================================================================

struct FrameEntry {
  const char* key;
  const uint8_t* data;  ///< PROGMEM pointer to kFrameSize bytes
};

static const FrameEntry kFrameTable[] = {
    {"blank", kBlank},
    {"boot", kBoot},
    {"status", kStatus},
};

static constexpr uint8_t kFrameCount =
    sizeof(kFrameTable) / sizeof(kFrameTable[0]);

/// @brief Find a frame by string key.
/// @return PROGMEM pointer to frame data, or nullptr if not found.
inline const uint8_t* findFrame(const char* key) {
  if (!key) return nullptr;
  for (uint8_t i = 0; i < kFrameCount; i++) {
    if (strcmp(key, kFrameTable[i].key) == 0) {
      return kFrameTable[i].data;
    }
  }
  return nullptr;
}

